int substrLen(string X, string Y, int m, int n)
{
 
    int LCSuff[m + 1][n + 1];
    int result = 0;
 
    for (int i = 0; i <= m; i++)
    {
        for (int j = 0; j <= n; j++)
        {
            if (i == 0 || j == 0)
                LCSuff[i][j] = 0;
 
            else if (X[i - 1] == Y[j - 1]) {
                LCSuff[i][j] = LCSuff[i - 1][j - 1] + 1;
                result = max(result, LCSuff[i][j]);
            }
            else
                LCSuff[i][j] = 0;
        }
    }
    return result;
}

void setIO(string name = "") {
    ios_base::sync_with_stdio(0); cin.tie(0);
    
    if ((int)(name).size()) {
        freopen((name+".in").c_str(), "r", stdin);
        freopen((name+".out").c_str(), "w", stdout);
    }   
}


int main()
{
    setIO("");
    int t; cin >> t;
    while (t--) {
        string a, b; cin >> a >> b;
        const int asz = a.size(), bsz = b.size();
        cout << (asz + bsz) - (substrLen(a, b, asz, bsz)*2) << "\n";
    }

    return 0;
}
