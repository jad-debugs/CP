void setIO(string name = "") {
    ios_base::sync_with_stdio(0); cin.tie(0);
    
    if ((int)(name).size()) {
        freopen((name+".in").c_str(), "r", stdin);
        freopen((name+".out").c_str(), "w", stdout);
    }   
}


int main()
{
    setIO("");

    int n; cin >> n;

    int behind = 3, ans = 0;
    for (int i = 0; i < n; i++) {

        int x; cin >> x;
        if (!x || (x == behind && x != 3)) {
            ans++;
            behind = 0;
        }
        else if (x == 3)
            behind = 3 - behind;
        else
            behind = x;
    }

    cout << ans;

    return 0;
}

